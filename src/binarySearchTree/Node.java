package binarySearchTree;

public class Node {
	private Integer data;
	private Node Right;
	private Node Left;
	
	public Integer getData() {
		return data;
	}
	public void setData(Integer data) {
		this.data = data;
	}
	public Node getRight() {
		return Right;
	}
	public void setRight(Node right) {
		Right = right;
	}
	public Node getLeft() {
		return Left;
	}
	public void setLeft(Node left) {
		Left = left;
	}
	
	

}
